	//--------------------------------------//

	//==========================================================================//
	//	関数																		//
	//==========================================================================//

	//--------------------------------------//
	//	初期化処理							//
	//--------------------------------------//

	//--------------------------------------//
	//	HighScore保存						//
	//--------------------------------------//
	public void SaveHighScore(int highScore)
	{
		PlayerPrefs.SetInt("HighScore", highScore);
		PlayerPrefs.Save();
	}

	//--------------------------------------//
	//	HighScore読み出し					//
	//--------------------------------------//
	public int ReadHighScore()
	{
		int ret;

		ret = PlayerPrefs.GetInt("HighScore", 0);

		return ret;
	}

